void MAX7219_Write_Serial_Data(uint16_t Data)
{
	uint8_t i = 0;

	for(i=0; i<16; i++){
		MAX7219_SCLK_Clr();
		
		if(Data&0x8000){
			MAX7219_SDIN_Set();
		}else{
			MAX7219_SDIN_Clr();
		}

		Data = Data<<1;

		MAX7219_SCLK_Set();
	}
}

/*
* Cnt: Count of MAX7291
* Addr: MAX7291 Register address
* Data: Write data to register
*/
void MAX7219_Write(uint8_t Cnt, uint8_t Addr, uint8_t Data)
{
	uint8_t i = 0;
	uint16_t dout = (Addr<<8|Data);
	MAX7219_CS_Clr();
	for(i=0; i<Cnt; i++){
		MAX7219_Write_Serial_Data(dout);
	}
	MAX7219_CS_Set();
}

/*
* Cnt: Count of MAX7291
*/
void MAX7219_Init(uint8_t Cnt)
{
	MAX7219_Write(Cnt, MAX7219_REG_DECODE_MODE, 0x00);       	//No decode for digits 7¨C0
	MAX7219_Write(Cnt, MAX7219_REG_INTENSITY, 0x04);       		//Intensity: 0x00 ~ 0x0F
	MAX7219_Write(Cnt, MAX7219_REG_SCAN_LIMIT, 0x07);       		//Display digits 0 1 2 3 4 5 6 7
	MAX7219_Write(Cnt, MAX7219_REG_SHUTDOWN, 0x01);       			//Shutdown Mode(0x00) Normal Operation(0x01)
	MAX7219_Write(Cnt, MAX7219_REG_DISPLAY_TEST, 0x00);       	//Normal Operation(0x00) Display Test Mode(0x01)
}

