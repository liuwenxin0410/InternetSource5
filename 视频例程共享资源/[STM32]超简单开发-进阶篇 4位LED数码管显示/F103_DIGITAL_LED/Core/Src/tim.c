static uint8_t LEDNUM[4] = {0xC7, 0xC0, 0xC1, 0x86};
void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)//0.1ms 中断
{
	static uint16_t usTimerCnt = 0;
	static uint8_t pos = 0;
	if(TIM3 == htim->Instance){
		usTimerCnt++;
		if(20 == usTimerCnt){//20*0.1ms = 0.002s = 500Hz
			usTimerCnt = 0;
//			Show_Digital_LED(LEDNUM[pos], pos);
			Show_Digital_LED_2(LEDNUM[pos], pos);
			pos++;
			if(4 == pos) pos = 0;
		}
	}
}

void TIM3_IT_Start(void)
{
	HAL_TIM_Base_Start_IT(&htim3);
}

void Update_LED_BUF(uint8_t num, uint8_t pos)
{
	if(4 <= pos )return;
	LEDNUM[pos] = num;
}

